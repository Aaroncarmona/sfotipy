public class Amigo {


    private String nombre;
    private String twitter;
    private String ultimacanción;

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getTwitter() {
        return twitter;
    }

    public void setTwitter(String twitter) {
        this.twitter = twitter;
    }

    public String getUltimacanción() {
        return ultimacanción;
    }

    public void setUltimacanción(String ultimacanción) {
        this.ultimacanción = ultimacanción;
    }



}
